package com.dapeng.controller.form;

/**
 * 注册form
 * @author lixf
 *
 */
public class RegistForm {
	
	String username;
	
	String password;
	
	String email;
	
	String phone;
	
	String birthday;
	
	String sex;
	
	String pswquestion;
	
	String pswanswer;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getPswquestion() {
		return pswquestion;
	}
	public void setPswquestion(String pswquestion) {
		this.pswquestion = pswquestion;
	}
	public String getPswanswer() {
		return pswanswer;
	}
	public void setPswanswer(String pswanswer) {
		this.pswanswer = pswanswer;
	}

}
